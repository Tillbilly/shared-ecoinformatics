package au.edu.aekos.shared.data.infomodel;

public class MetaInfoExtractorException extends Exception {

	private static final long serialVersionUID = 8349712195513397748L;

	public MetaInfoExtractorException() {
		super();
	}

	public MetaInfoExtractorException(String message, Throwable cause) {
		super(message, cause);
	}

	public MetaInfoExtractorException(String message) {
		super(message);
	}

	public MetaInfoExtractorException(Throwable cause) {
		super(cause);
	}

}
