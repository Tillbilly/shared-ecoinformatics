package au.edu.aekos.shared.service.quest;

public class ControlledVocabularyNotFoundException extends Exception {

	public ControlledVocabularyNotFoundException() {
		super();
	}

	public ControlledVocabularyNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ControlledVocabularyNotFoundException(String arg0) {
		super(arg0);
	}

	public ControlledVocabularyNotFoundException(Throwable arg0) {
		super(arg0);
	}
}
