package au.edu.aekos.shared.service.s3;

public class ObjectStoreServiceException extends Exception {

	public ObjectStoreServiceException() {
	}

	public ObjectStoreServiceException(String arg0) {
		super(arg0);
	}

	public ObjectStoreServiceException(Throwable arg0) {
		super(arg0);
	}

	public ObjectStoreServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
